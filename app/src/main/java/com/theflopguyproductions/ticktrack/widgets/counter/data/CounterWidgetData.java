package com.theflopguyproductions.ticktrack.widgets.counter.data;

public class CounterWidgetData {

    int counterIdInteger, counterWidgetId, widgetTheme;
    String counterIdString;

    public int getWidgetTheme() {
        return widgetTheme;
    }

    public void setWidgetTheme(int widgetTheme) {
        this.widgetTheme = widgetTheme;
    }

    public int getCounterIdInteger() {
        return counterIdInteger;
    }

    public void setCounterIdInteger(int counterIdInteger) {
        this.counterIdInteger = counterIdInteger;
    }

    public int getCounterWidgetId() {
        return counterWidgetId;
    }

    public void setCounterWidgetId(int counterWidgetId) {
        this.counterWidgetId = counterWidgetId;
    }

    public String getCounterIdString() {
        return counterIdString;
    }

    public void setCounterIdString(String counterIdString) {
        this.counterIdString = counterIdString;
    }
}
