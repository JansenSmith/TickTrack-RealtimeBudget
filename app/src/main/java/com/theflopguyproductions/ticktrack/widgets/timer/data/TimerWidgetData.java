package com.theflopguyproductions.ticktrack.widgets.timer.data;

public class TimerWidgetData {

    int timerIdInteger, timerWidgetId, widgetTheme;
    String timerIdString;

    public int getTimerIdInteger() {
        return timerIdInteger;
    }

    public void setTimerIdInteger(int timerIdInteger) {
        this.timerIdInteger = timerIdInteger;
    }

    public int getTimerWidgetId() {
        return timerWidgetId;
    }

    public void setTimerWidgetId(int timerWidgetId) {
        this.timerWidgetId = timerWidgetId;
    }

    public int getWidgetTheme() {
        return widgetTheme;
    }

    public void setWidgetTheme(int widgetTheme) {
        this.widgetTheme = widgetTheme;
    }

    public String getTimerIdString() {
        return timerIdString;
    }

    public void setTimerIdString(String timerIdString) {
        this.timerIdString = timerIdString;
    }
}
